# print elements in a list
a = [4, 7, 3, 2, 5, 9]

for i, element in enumerate(a):
  print(f"The element at index {i} is {element}.")

#Output:
#The element at index 0 is 4.
#The element at index 1 is 7.
#The element at index 2 is 3.
#The element at index 3 is 2.
#The element at index 4 is 5.
#The element at index 5 is 9.