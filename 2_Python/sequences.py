name = "Harry"

print(name[0])  # H
print(name[1])  # a
print(name[2])  # r
print(name[3])  # r
print(name[4])  # y

# list
names = ["Harry", "Ron", "Hermione"]
print(names[0])  # Harry
print(names[1])  # Ron
print(names[2])  # Hermione

# tuple
coordinateX = 10.0
coordinateY = 20.0
coordinate = (coordinateX, coordinateY)
print(coordinate)  # (10.0, 20.0)
print(coordinate[0])  # 10.0
print(coordinate[1])  # 20.0
